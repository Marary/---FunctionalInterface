package Lambda.grammar;

/**
 * First, define a functional interface.
 * It doesn't matter what the method is now.
 * For demonstration convenience, we define a return value with two parameters
 * 首先，定义一个函数式接口。现在方法是什么无关紧要,我们为了展示方便就定义一个带两个参带返回值的。
 */
@FunctionalInterface
public interface funInterface {
        int fun(int a,int b);
}
